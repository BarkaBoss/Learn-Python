import math


def area_of_square(a):
    square = a ** 2
    return square


def area_of_rectangle(l, b):
    l * b
    return


def area_of_triangle(b, h):
    area = 1 / 2 * b * h
    return


def area_of_trapezoid(b1, b2, h):
    area = 1 / 2 * (b1 + b2) * h
    return


def area_of_circle(r):
    area = math.pi * r ** 2
    return


def circumference_of_circle(r):
    circumference = 2 * math.pi * r
    return


def surface_area_of_cube(a):
    surface_area = 6 * a ** 2
    return


def curved_area_of_cylinder(r, h):
    curved_area = 2 * math.pi * r * h
    return


def total_surface_area_of_cylinder(r, h):
    total_surface_area = 2 * math.pi * r(r + h)
    return


def volume_of_cylinder(r, h):
    volume = math.pi * r ** 2 * h
    return


def acceleration(v, u, t):
    acc = (v - u) / t
    return


def density(m, v):
    m / v
    return


